date_today = str(date.today())

channel_set = set()
article_urls = {}

# sanitises any weird data left over from the last running
if os.path.isfile("output.csv"):
    os.remove("output.csv")

# Read channels to scan from the config file (one channel per line)
with open("config") as file:
    while line := file.readline():
        channel_set.add(line.rstrip())

# Scraps all messages sent for today in all channels listed in the config, and
# dumps the contents of the messages into output.csv
for channel_id in channel_set:
    print("Scraping: " + channel_id)
    sleuth = Sleuth(
        api_id = API_ID,
        api_hash = API_HASH,
        username = channel_id,
        start_date = date_today,
        end_date = date_today,
        print_to_console = True # not rly needed i just like the ouput ;P
    )

    # Generate a dict with messages, media, & metadata
    data = sleuth.dig()
    dig_output = pandas.DataFrame(data)
    dig_output.to_csv('output.csv', mode='a', header=False, index=False)

# Removes and duplicate entries from output.csv
raw_dump_input = pandas.read_csv('output.csv', header=None, index_col=False)
clean_data = raw_dump_input.drop_duplicates()
clean_data.to_csv('output.csv', mode='w', header=False, index=False)

# Parses output.csv and extracts article urls; stores them back into output.csv
url_index = 0
with open("output.csv") as file:
    while line := file.readline():
        article_urls[url_index] = (re.search(url_regex, line).group())+"\n"
        url_index += 1

url_output = pandas.DataFrame([article_urls])
url_output.to_csv('article_urls.csv', mode='w', header=False, index=False)
